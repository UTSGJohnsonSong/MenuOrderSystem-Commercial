/*
 * 模糊搜索：不要求精准输入。
 * - "鸡翅" → 可乐鸡翅、烤鸡翅…（菜名包含）
 * - "肉"   → 红烧肉、糖醋排骨…（菜名或标签命中，"猪肉/牛肉"等标签都含"肉"）
 * - "西红柿鸡蛋" → 番茄炒蛋…（逐字命中，容忍别名/乱序）
 * 返回按相关度降序；空查询返回全部（保持原始分类顺序）。
 */
export function searchLibrary(query: string, dishes: LibraryDish[] = LIBRARY_DISHES): LibraryDish[] {
  const q = query.trim().toLowerCase();
  if (!q) return dishes;

  // 常见同义词归一，两边都做（查询和数据里写法不一致时兜底）
  const ALIASES: [string, string][] = [
    ["西红柿", "番茄"], ["土豆", "马铃薯"], ["鸡蛋", "蛋"], ["洋芋", "土豆"], ["青椒", "辣椒"],
  ];
  const normalize = (s: string) => {
    let out = s;
    for (const [a, b] of ALIASES) out = out.split(a).join(b);
    return out;
  };
  const nq = normalize(q);

  const scored: { d: LibraryDish; s: number }[] = [];
  for (const d of dishes) {
    const name = d.name.toLowerCase();
    const nname = normalize(name);
    let s = 0;

    if (name === q || nname === nq) s = 1000;
    else if (name.includes(q) || nname.includes(nq)) {
      // 名字越短匹配越"纯"，排前面（搜"鸡翅"时"烤鸡翅"排在"蒜香鸡翅"前）
      s = 500 + Math.max(0, 30 - d.name.length);
    } else {
      for (const t of d.tags) {
        const tl = t.toLowerCase();
        if (tl === q) { s = Math.max(s, 300); break; }
        if (tl.includes(q) || q.includes(tl)) s = Math.max(s, 220);
      }
      if (s === 0 && d.ingredients.toLowerCase().includes(q)) s = 120;
      if (s === 0) {
        // 逐字匹配：查询的每个字都出现在 名字+标签 里就算命中（容忍乱序和多余字）
        const hay = nname + normalize(d.tags.join("").toLowerCase());
        const chars = [...nq].filter(c => c.trim());
        if (chars.length >= 1) {
          const hit = chars.filter(c => hay.includes(c)).length;
          if (hit === chars.length) s = 80;
          else if (chars.length >= 3 && hit / chars.length >= 0.6) s = Math.round(50 * (hit / chars.length));
        }
      }
    }
    if (s > 0) scored.push({ d, s });
  }

  scored.sort((a, b) => b.s - a.s);
  return scored.map(x => x.d);
}
